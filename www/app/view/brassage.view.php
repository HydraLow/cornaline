        <div class="text">
            <h2>Le procédé de Fabrication</h2>
            <p>La préparation inclut le malt, l'orge, les houblons, l'eau, la levure et les gousses de vanille.</p>
        </div>
    </section>
    <hr>
    <section class="step">
        <img src="../../public/images/concassage.png" alt="Moulin à malt">
        <div class="text">
            <h2>La Moulure et le Concassage</h2>
            <p>Casser les grains de malt pour libérer leur enveloppe sans les transformer en poudre fine. Ceci facilite l'extraction des sucres durant l'empâtage, et une bonne mouture améliore le rendement de la bière.</p>
        </div>
    </section>
    <hr>
    <section class="step">
        <img src="../../public/images/emphatage.jpg" alt="Malt et eau">
        <div class="text">
            <h2>Infusion et Empathage</h2>
            <p>L'infusion de la bière Cornaline révèle des notes subtiles et profondes, mêlant harmonieusement les arômes maltés et les touches fruitées qui caractérisent cette bière artisanale.</p>
            <p>Mélanger le malt concassé avec de l’eau chaude pour activer les enzymes. Ces enzymes transforment l’amidon des grains en sucres fermentescibles. La température est contrôlée pour influencer le profil de la bière. Cett étape produit le moût sucré nécessair à la fermentation.</p>
        </div>
    </section>
    <hr>
    <section class="step">
        <img src="../../public/images/infusion.jpg" alt="Concassage malt">
        <div class="text">
            <h2>Rinçage</h2>
            <p>Extraire un maximum de sucres restés dans les grains après l’empâtage. On verse de l’eau chaude sur la drêche pour récupérer ces sucres. Cela augmente le rendement sans trop diluer le moût. C’est une étape importante avant la cuisson.</p>
        </div>
    </section>
